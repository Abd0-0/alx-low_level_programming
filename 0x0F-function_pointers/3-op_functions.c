#include "3-calc.h"

/**
 * op_add - adds two number.
 *
 * @a: the 1at number.
 * @b: the 2nd number.
 *
 * Return: the calculation output.
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - sub two number.
 *
 * @a: the 1at number.
 * @b: the 2nd number.
 *
 * Return: the calculation output.
 *
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mult two number.
 *
 * @a: the 1at number.
 * @b: the 2nd number.
 *
 * Return: the calculation output.
 *
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two number.
 *
 * @a: the 1at number.
 * @b: the 2nd number.
 *
 * Return: the calculation output.
 *
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a % b.
 *
 * @a: the 1at number.
 * @b: the 2nd number.
 *
 * Return: the calculation output.
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}
